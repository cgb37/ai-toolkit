    fi

    # Batten down the hatches — only one argument, ye scallywag!
    if [[ $# -gt 1 ]]; then
        echo -e "${YELLOW}Arrr! Too many arguments! Wrap yer string in quotes, landlubber!${NC}" >&2
        usage
        exit 1
    fi

    local input="$1"

    # Here be the treasure — compute the slug!
    local result
    result="$(slugify "$input")"

    # Hoist the colors and print the slug on its own line for easy plunderin'
