# Colors for the treasure map (output formatting)
readonly GREEN='\033[0;32m'
readonly YELLOW='\033[1;33m'
readonly NC='\033[0m'

################################################################################
# Usage
################################################################################

usage() {
    # Shiver me timbers! Show the crew how to use this tool
    cat <<EOF

Usage:
  $(basename "$0") "Your String Here"

Description:
  Converts a string into a URL-safe slug.

  A slug will:
    - Be converted to lowercase
    - Have spaces replaced with hyphens
    - Have non-alphanumeric/non-hyphen characters removed
    - Have consecutive hyphens collapsed into one
    - Have leading and trailing hyphens stripped

Examples:
  $(basename "$0") "Hello World"          → hello-world
  $(basename "$0") "It's a Pirate's Life!" → its-a-pirates-life
  $(basename "$0") "The Quick Brown Fox"   → the-quick-brown-fox

EOF
}

################################################################################
# Slugify Function
################################################################################

slugify() {
    local input="$1"

    # Arrr! One mighty pipeline does the work of five scallywags:
    #   1. Lowercase — no uppercasin' landlubbers allowed!
    #   2. Spaces → hyphens — walk the plank, spaces!
    #   3. Strip non-alphanumeric/non-hyphen chars — overboard, ye scurvy characters!
    #   4. Collapse consecutive hyphens — no double-crossers on this ship!
    #   5. Trim leading/trailing hyphens — trim the sails, matey!
    echo "$input" \
        | tr '[:upper:]' '[:lower:]' \
        | tr ' ' '-' \
        | sed 's/[^a-z0-9-]//g; s/-\{2,\}/-/g; s/^-*//; s/-*$//'
}

